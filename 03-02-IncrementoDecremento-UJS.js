let a = 3, b = 2;
let z = a + b;
console.log(z);
//Incremento
//Pre-incremento (el operador ++ antes de la variable)
console.log(a);
console.log(z);
z = ++a;
console.log(a);
console.log(z);

//Post-incremento (el operador ++ despues de la variable)
console.log(b);
console.log(z);
z = b++;
console.log(b);
console.log(z);

//Decremento
//Predecremento
console.log(a);
console.log(z);
z = --a;
console.log(a);
console.log(z);

//Postdecremento
console.log(b);
console.log(z);
z = b--;
console.log(b);
console.log(z);